var plusOne = function(digits) {
    for (let i = digits.length - 1; i >= 0; i--) {
        if (digits[i] < 9) {
            digits[i]++;
            return digits;
        }
        digits[i] = 0;
    }

    // if all digits were 9
    digits.unshift(1);
    return digits;
};


let digits = [4,3,2,1]

const value = plusOne(digits)
